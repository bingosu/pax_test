void hexdump(const char *pzTitle, const byte *buff, long len)
{
	
	char tg[30];
	char *ptr;
	unsigned int i;
	unsigned char n, lenbuff,index=0;
	ptr = (char *)buff;
	n = 0;

	if (!mtiIsValildPrinter())
	{
		mtiInitializePrinter();
	}
	lenbuff=len;
	memset(tg,0x00,sizeof(tg));
	strcpy(tg, pzTitle);
	strcat(tg,"\n");
	debugPrint(tg);
	sprintf(tg, "LEN:%u Addr:%p", lenbuff, buff);
	strcat(tg, "\n");
	debugPrint(tg);
	memset( tg, 0x00, 25 );
	for(i=0; i<lenbuff; i++)
	{
		if (n == 0)
			sprintf(&tg[n*3], "%02u|%02X ", index, *(unsigned char *)&ptr[i]);
		else
			sprintf(&tg[n*3+3], "%02X ", *(unsigned char *)&ptr[i]);

		if( n == 6 )
		{
			strcat(tg, "\n");
			debugPrint(tg);
			index++;
			n = 0;
			memset( tg, 0x00, 25 );
		}
		else
			n++;
	}
	if (n != 0)
		{
		strcat(tg, "\n");
		debugPrint(tg);
		}
	Telium_Pprintf("\x0D\x0A");
	Telium_Pprintf("\x0D\x0A");
	Telium_Pprintf("\x0D\x0A");
	Telium_Pprintf("\x0D\x0A");
	Telium_Pprintf("\x0D\x0A");
	Telium_Ttestall(PRINTER, 1*100);										
	mtiFinalizePrinter();

}
void hex_dump_char(const char *pzTitle, const byte *buff, long len)
{
	ulong x, y;
	char buffer[1024];
	char buffer2[1024];
	char szBuff[32+1];
	char szBuffTemp[1024];
	char ascii[16+1];
	byte start;
	int slen;
	int pos;
	byte *buffdata;
	int counter;		// used to count line number
	CHAR *cpCourrier = NULL;

	start = 0;
	counter = 0;
	pos = 0;

	if (!mtiIsValildPrinter())
	{
		mtiInitializePrinter();
	}

#ifdef _MOCKUP_MODE_
	cpCourrier = LoadFont("/SYSTEM/COURRIER.PGN");
#else
	cpCourrier = LoadFont("/SYSTEM/COUR.PGN");
#endif

#ifdef _TETRA_
	cpCourrier = LoadFont("/SYSTEM/COUR.T3P");
#endif

	dmsg("LoadFont Result = %p", cpCourrier);

	DefCurrentFont(cpCourrier);

	MEM_ZERO(buffer);
	MEM_ZERO(ascii);
	MEM_ZERO(szBuff);
	MEM_ZERO(szBuffTemp);

	// copy the data to another memory
	buffdata = (byte *)buff;

	// print title, len and memory address first
 	prtPrint(_COMPRESSED_, (char*)pzTitle);
	sprintf(szBuff, "Len:%d\t\t\t\tAddr:%p", (int)len, buff);
	prtPrint(_COMPRESSED_, szBuff);

	// if there is no data in the buffer
	// memory is empty
	if (len == 0 || !buffdata) return;

	for (x=0; x<len; x++)
	{
		if (start == 0) // first column w/ line number
		{
			sprintf(&buffer[0], "%02d | %02X ", counter, *buffdata);
			counter++;
		}
		else // next column
		{
			slen = strlen(buffer);
			sprintf(&buffer[slen], "%02X ", *buffdata);
		}

		// get ascii character
		sprintf(&ascii[pos], "%c", ((*buffdata <= 0x1f || *buffdata > 0x7f) ? '.' : *buffdata));

		// next byte
		*buffdata++;
		pos++;

		if (start == 9)
		{
			start = 0;
			pos = 0;

			sprintf(szBuffTemp, "%s %s", buffer, ascii);
			prtPrint(_COMPRESSED_, szBuffTemp);

			MEM_ZERO(buffer);
			MEM_ZERO(ascii);
			MEM_ZERO(szBuffTemp);
		}
		else
			start++;
	}

	if (start <= 9)
	{
		MEM_ZERO(buffer2);
		MEM_ZERO(szBuffTemp);

		memcpy(buffer2, buffer, strlen(buffer));
		for (y = 0; y < (35 - strlen(buffer2)); y++)
		{
			slen = strlen(buffer);
			sprintf(&buffer[slen], "%s", " "); // fill the empty column with space
		}

		sprintf(szBuffTemp, "%s %s", buffer, ascii);
		prtPrint(_COMPRESSED_, szBuffTemp); // print last bytes
	}

	prtPrint(_COMPRESSED_, " ");
	mtiSleep(TIMEOUT_1S);

	if (cpCourrier != NULL)
	{
		UnloadFont(cpCourrier);
	}

	mtiFinalizePrinter();
}
