INT mtiUpdatePrintQueue(tMtiMap *tpPrtMap, INT iIndex, CHAR cAttribute, CHAR *cpContents)
{
	CHAR caData[50];
	CHAR *cpData = caData;

	if (tpPrtMap == NULL)
	{
		return RTN_ERROR;
	}

	mtiMemset(caData, 0, sizeof(caData));
	*cpData++ = cAttribute;
	cpData += mtiStrcpy(cpData, cpContents, P_MAX_COULUM);

	mtiMapPutString(tpPrtMap, iIndex, caData);
	return RTN_SUCCESS;
}


INT mtiAddPrintQueue(tMtiMap *tpPrtMap, CHAR cAttribute, CHAR *cpContents)
{
	CHAR caData[1024];
	CHAR *cpData = caData;

	if (tpPrtMap == NULL)
	{
		return RTN_ERROR;
	}

	mtiMemset(caData, 0, sizeof(caData));
	*cpData++ = cAttribute;

	if (CHK_BIT(cAttribute, P_MODE_QRCODE))
	{
		cpData += mtiStrcpy(cpData, cpContents, sizeof(caData));
	}
	else
	{
		cpData += mtiStrcpy(cpData, cpContents, P_MAX_COULUM);
	}

	mtiMapPutString(tpPrtMap, tpPrtMap->iMapCount, caData);
	return RTN_SUCCESS;
}

INT mtiGetPrintStatus()
{
	UCHAR ucStatus = 0;
	INT iResult = 0;

	if (mtPrinter != NULL)
	{
		Telium_Status(mtPrinter, &ucStatus);

//		dmsg("ucStatus = %d", ucStatus);
		if ((ucStatus & PAPER_OUT) ==  PAPER_OUT)
		{
			return P_STATUS_NO_PAPER;
		}

		if ((ucStatus & PRINT_ERROR) ==  PRINT_ERROR)
		{
			return P_STATUS_PRINT_ERROR;
		}

		if ((ucStatus & PRINTER_BAT_LOW) ==  PRINTER_BAT_LOW)
		{
			return P_STATUS_LOW_BATTERY;
		}
	}

	dmsg("mtiGetPrintStatus() Result = %d", iResult);
	return iResult;
}

